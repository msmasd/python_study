# coding=utf-8
a = 1000
print(a)  # 1000

b = 157.93
print(b)  # 157.93

c = 5.
print(c)  # 5.0

d = -.7
print(d)  # -0.7

# 유효숫자e지수 = 유효숫자 * 10^지수

# 10억의 지수 표현 방식
e = 1e9
print(e)  # 1000000000.0

f = 75.25e1  # 75.25 * 10 ^ 1 = 752.5
print(f)  # 752.5

g = 3954e-3  # 3954 * 10 ^ -3 = 3.954
print(g)  # 3.954

# 사칙연산
h = 7
i = 3

print(h / i)  # 나누기(2)
print(h % i)  # 나머지(1)
print(h // i)  # 몫(2)
print(h ** i)  # 거듭제곱(343)

